    private void populateTable(){
        DefaultTableModel dtm=(DefaultTableModel)tblaccounts.getModel();
        dtm.setRowCount(0);
        //for(Account account:accDir.getAccountList()){
        for(Account account:accDir.getAccountList()){
            Object[] row=new Object[4];
            row[0]=account;
            //JOptionPane.showMessageDialog(null,row[0]);
            row[1]=account.getRounum();
            row[2]=account.getAccnum();
            row[3]=account.getBalance();
            
            dtm.addRow(row);
        }
    }

