    private void searchBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBTNActionPerformed
        // TODO add your handling code here:
        Account result=accDir.accSearch(searchTXT.getText());
        if(result==null){
            JOptionPane.showMessageDialog(null,"The account number you searched does not exist!", "information",JOptionPane.INFORMATION_MESSAGE);
        }
        else{
           ViewDetailsPane panel=new ViewDetailsPane(userProcessContainer,result);
           userProcessContainer.add("ViewDetailsPane",panel);
           CardLayout layout=(CardLayout) userProcessContainer.getLayout();
           layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_searchBTNActionPerformed

