    private void deleteBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBTNActionPerformed
        // TODO add your handling code here:
        int selerow=tblaccounts.getSelectedRow();
        if (selerow>=0){
            int dialogButton=JOptionPane.YES_NO_OPTION;
            int dialogResult=JOptionPane.showConfirmDialog(null,"Would you like to delete this account details?","WARNING",dialogButton);
            if(dialogResult==JOptionPane.YES_OPTION){
                Account account=(Account) tblaccounts.getValueAt(selerow,0);
                accDir.deleteAcc(account);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null,"Please select a row you want to delete!","WARNING",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_deleteBTNActionPerformed

    private void viewBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewBTNActionPerformed
        // TODO add your handling code here:
        int selerow=tblaccounts.getSelectedRow();
        if(selerow<0){
          JOptionPane.showMessageDialog(null,"Please select a row you want to view details!","WARNING",JOptionPane.WARNING_MESSAGE);  
        }
        else{
           Account account=(Account) tblaccounts.getValueAt(selerow,0);
           ViewDetailsPane panel=new ViewDetailsPane(userProcessContainer,account);
           userProcessContainer.add("ViewDetailsPane",panel);
           CardLayout layout=(CardLayout) userProcessContainer.getLayout();
           layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_viewBTNActionPerformed


