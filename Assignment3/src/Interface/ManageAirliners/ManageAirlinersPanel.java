    private void viewairlBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewairlBTNActionPerformed
        // TODO add your handling code here:
        int row = TBLmngairl.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a row from the table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Airliners airl = (Airliners)TBLmngairl.getValueAt(row,0);
        ViewAirlinersPanel panel = new ViewAirlinersPanel(this,CardSecePanel, airl);
        CardSecePanel.add("ViewAirlinersPanel", panel);
        CardLayout layout = (CardLayout)CardSecePanel.getLayout();
        layout.next(CardSecePanel);
        
    }//GEN-LAST:event_viewairlBTNActionPerformed

