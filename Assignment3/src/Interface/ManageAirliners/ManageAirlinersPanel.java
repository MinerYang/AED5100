    public void populateTable(){
        //Clear airliner table 
        //System.out.println("Populate");
        DefaultTableModel dtm=(DefaultTableModel)TBLmngairl.getModel();
        dtm.setRowCount(0);
        //System.out.println(airlDir.getAirlinersList().size());
        for(Airliners airl:airlDir.getAirlinersList()){
            Object[] row=new Object[2];
            row[0]=airl;
            row[1]=airl.getAddress();
            //JOptionPane.showMessageDialog(null,row[0]);       
            dtm.addRow(row);
        }
    }

